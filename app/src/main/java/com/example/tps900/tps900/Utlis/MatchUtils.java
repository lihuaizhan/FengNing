    /**
     * 校验是否是手机号
     *
     * @param mobileNumber
     * @return
     */

    public static boolean isMobileNO(String mobileNumber) {
        Matcher m = NUMBER_PHONE.matcher(mobileNumber);
        return m.matches();
    }

